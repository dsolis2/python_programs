# count down functions


def count_down(num_1):
    print("Count down")
    print("---------")
    while num_1 > 0 :
        print(num_1)
        num_1 = num_1 - 1
       

# count up
def count_up(num_2) : 
    num_2 = 0
    print("Count up")
    print("----------")
    while num_2 < 10:
        print(num_2)
        num_2 = num_2 + 1
       


